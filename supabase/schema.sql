create extension if not exists pgcrypto;

create or replace function public.current_clinic_id()
returns uuid
language sql
stable
as $$
  select nullif(current_setting('app.current_clinic_id', true), '')::uuid;
$$;

create table if not exists clinics (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  subdomain text unique not null,
  address text,
  phone text,
  subscription_tier text not null check (subscription_tier in ('free', 'pro', 'enterprise')),
  created_at timestamptz not null default now()
);

create table if not exists staff (
  id uuid primary key default gen_random_uuid(),
  clinic_id uuid not null references clinics(id) on delete cascade,
  role text not null check (role in ('doctor', 'receptionist', 'admin')),
  full_name text not null,
  specialty text,
  auth_user_id uuid unique references auth.users(id)
);

create table if not exists patients (
  id uuid primary key default gen_random_uuid(),
  auth_user_id uuid unique references auth.users(id),
  full_name text not null,
  phone text unique not null,
  date_of_birth date,
  national_id text
);

create table if not exists doctor_availability (
  id uuid primary key default gen_random_uuid(),
  staff_id uuid not null references staff(id) on delete cascade,
  day_of_week int not null check (day_of_week between 0 and 6),
  start_time time not null,
  end_time time not null,
  slot_duration_minutes int not null default 30 check (slot_duration_minutes > 0)
);

create table if not exists appointments (
  id uuid primary key default gen_random_uuid(),
  clinic_id uuid not null references clinics(id) on delete cascade,
  patient_id uuid not null references patients(id) on delete cascade,
  staff_id uuid not null references staff(id) on delete cascade,
  scheduled_at timestamptz not null,
  duration_minutes int not null default 30 check (duration_minutes > 0),
  status text not null check (status in ('pending', 'confirmed', 'completed', 'cancelled', 'no_show')),
  reason text,
  created_at timestamptz not null default now(),
  cancelled_reason text
);

create table if not exists medical_records (
  id uuid primary key default gen_random_uuid(),
  patient_id uuid not null references patients(id) on delete cascade,
  appointment_id uuid not null references appointments(id) on delete cascade,
  notes text,
  attachments jsonb not null default '[]'::jsonb,
  created_by uuid references staff(id),
  created_at timestamptz not null default now()
);

create table if not exists payments (
  id uuid primary key default gen_random_uuid(),
  appointment_id uuid not null references appointments(id) on delete cascade,
  amount_iqd numeric not null check (amount_iqd >= 0),
  provider text not null check (provider in ('zaincash', 'qicard', 'cash')),
  status text not null check (status in ('pending', 'paid', 'failed', 'refunded')),
  transaction_ref text,
  created_at timestamptz not null default now()
);

create table if not exists audit_logs (
  id bigint generated by default as identity primary key,
  clinic_id uuid,
  actor_user_id uuid,
  action text not null,
  entity_type text not null,
  entity_id text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create unique index if not exists unique_active_slot
  on appointments(staff_id, scheduled_at)
  where status <> 'cancelled';

create index if not exists idx_staff_clinic_id on staff(clinic_id);
create index if not exists idx_appointments_clinic_id on appointments(clinic_id);
create index if not exists idx_appointments_patient_id on appointments(patient_id);
create index if not exists idx_appointments_staff_id on appointments(staff_id);
create index if not exists idx_medical_records_patient_id on medical_records(patient_id);
create index if not exists idx_payments_appointment_id on payments(appointment_id);
create index if not exists idx_audit_logs_clinic_id_created_at on audit_logs(clinic_id, created_at desc);


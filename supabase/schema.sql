alter table staff enable row level security;
alter table patients enable row level security;
alter table doctor_availability enable row level security;
alter table appointments enable row level security;
alter table medical_records enable row level security;
alter table payments enable row level security;
alter table audit_logs enable row level security;

create policy if not exists clinics_read_own on clinics
for select to authenticated
using (id = public.current_clinic_id());

create policy if not exists staff_read_own_clinic on staff
for select to authenticated
using (clinic_id = public.current_clinic_id());

create policy if not exists staff_manage_own_clinic on staff
for all to authenticated
using (clinic_id = public.current_clinic_id())
with check (clinic_id = public.current_clinic_id());

create policy if not exists patient_self_access on patients
for select to authenticated
using (auth_user_id = auth.uid());

create policy if not exists patient_self_update on patients
for update to authenticated
using (auth_user_id = auth.uid())
with check (auth_user_id = auth.uid());


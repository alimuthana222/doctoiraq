create or replace function public.log_audit_event(
  p_clinic_id uuid,
  p_action text,
  p_entity_type text,
  p_entity_id text,
  p_metadata jsonb default '{}'::jsonb
)
returns void
language plpgsql
security definer
as $$
begin
  insert into audit_logs (clinic_id, actor_user_id, action, entity_type, entity_id, metadata)
  values (p_clinic_id, auth.uid(), p_action, p_entity_type, p_entity_id, coalesce(p_metadata, '{}'::jsonb));
end;
$$;

create or replace function public.book_appointment(
  p_staff_id uuid,
  p_patient_id uuid,
  p_scheduled_at timestamptz,
  p_reason text default null,
  p_duration_minutes int default 30
)
returns appointments
language plpgsql
security definer
as $$
declare
  v_clinic_id uuid;
  v_existing appointments;
  v_appointment appointments;
begin
  select clinic_id
    into v_clinic_id
  from staff
  where id = p_staff_id
    and role = 'doctor';

  if v_clinic_id is null then
    raise exception 'Doctor not found';
  end if;

  select *
    into v_existing
  from appointments
  where staff_id = p_staff_id
    and scheduled_at = p_scheduled_at
    and status <> 'cancelled'
  for update;

  if found then
    raise exception 'Slot already booked';
  end if;

  insert into appointments (
    clinic_id,
    patient_id,
    staff_id,
    scheduled_at,
    duration_minutes,
    status,
    reason
  )
  values (
    v_clinic_id,
    p_patient_id,
    p_staff_id,
    p_scheduled_at,
    p_duration_minutes,
    'pending',
    p_reason
  )
  returning * into v_appointment;

  perform public.log_audit_event(
    v_clinic_id,
    'appointment.created',
    'appointments',
    v_appointment.id::text,
    jsonb_build_object('patient_id', p_patient_id, 'staff_id', p_staff_id)
  );

  return v_appointment;
end;
$$;
